/**
 * add - do basic maths operators
 * Return: result
 */
int add(int a, int b)
{
        return (a + b);
}

/**
 * sub - do basic maths operators
 * Return: result
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * div - do basic maths operators
 * Return: result
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mul - do basic maths operators
 * Return: result
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * mod - do basic maths operators
 * Return: result
 */
int mod(int a, int b)
{
        return (a % b);
}
