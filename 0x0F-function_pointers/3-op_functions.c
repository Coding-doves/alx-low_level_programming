#include "3-calc.h"

/**
 * op_add -  a function to sum-up number
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - a function to subtract number
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - a function to multiply number
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - a function to divide number
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - a function to return remainder to divided number
 * @a: num1
 * @b: num2
 * Return: returns the sum of a and b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
